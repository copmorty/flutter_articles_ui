class Article {
  final String id;
  final String title;
  final String description;
  final String image;
  final String postTime;
  final String reads;
  final String fans;

  const Article({
    required this.id,
    required this.title,
    required this.description,
    required this.image,
    required this.postTime,
    required this.reads,
    required this.fans,
  });
}
